// .style
// manipulate element's style

// tag.style.color = "blue";
// styles the element "tag" with the color blue


// .classList
// allows you to add multiple styles with just a single class to stay away from DRY

// add a class
// tag.classList.add("class-name")

// remove a class
// tag.classList.remove("class-name")

// toggle a class
// tag.classList.toggle("class-name")


// .textContent
// returns a string of all text contained in a given element

// retrieve textContent
// tag.textContent returns text in tag

// alter text
// tag.textContent = "alfjlds;fjlsdafj"


// .innerHTML
// returns a string of all HTML contained in a given element

// tag.innerHTML returns ex. "This is an <strong>awesome</strong> paragraph"


// .getAttribute
// to read and write attributes lide src or href

// link.getAttribute("href") returns href of link ex. www.google.com

// change attribute - works the same with src or any other attribute
// link.setAttribute("href", "www.dogs.com")
// changes href from www.google.com to www.dogs.com
